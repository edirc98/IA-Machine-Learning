def NumToMonth(num):
    if num == 1:
        return "Enero"
    elif num == 2:
        return "Febrero"
    elif num == 3:
        return "Marzo"
    elif num == 4:
        return "Abril"
    elif num == 5:
        return "Mayo"
    elif num == 6:
        return "Junio"
    elif num == 7:
        return "Julio"
    elif num == 8:
        return "Agosto"
    elif num == 9:
        return "Septiembre"
    elif num == 10:
        return "Octubre"
    elif num == 11:
        return "Noviembre"
    elif num == 12:
        return "Diciembre"

print(NumToMonth(6))